// Helper functions
export const getBackgroundsByCategory = (
  category: BackgroundMedia["category"]
) => {
  return BACKGROUND_IMAGES.filter((bg) => bg.category === category);
};

export const getRandomBackground = (category?: BackgroundMedia["category"]) => {
  const backgrounds = category
    ? getBackgroundsByCategory(category)
    : BACKGROUND_IMAGES;

  return backgrounds[Math.floor(Math.random() * backgrounds.length)];
};

export const findBackgroundById = (id: string) => {
  return BACKGROUND_IMAGES.find((bg) => bg.id === id);
};

// Thumbnail generator helper
export const getThumbnailUrl = (src: string, size: number = 300) => {
  // For Unsplash URLs, we can modify the parameters for thumbnails
  if (src.includes("unsplash.com")) {
    return src.replace(/w=\d+&h=\d+/, `w=${size}&h=${Math.round(size * 0.6)}`);
  }
  return src;
};
